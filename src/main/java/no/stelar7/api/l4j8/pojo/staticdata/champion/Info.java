package no.stelar7.api.l4j8.pojo.staticdata.champion;

public class Info
{
    Integer attack;
    Integer defence;
    Integer difficulty;
    Integer magic;

    /**
     * Gets the attack.
     *
     * @return the attack
     */
    public Integer getAttack()
    {
        return attack;
    }

    /**
     * Gets the defence.
     *
     * @return the defence
     */
    public Integer getDefence()
    {
        return defence;
    }

    /**
     * Gets the difficulty.
     *
     * @return the difficulty
     */
    public Integer getDifficulty()
    {
        return difficulty;
    }

    /**
     * Gets the magic.
     *
     * @return the magic
     */
    public Integer getMagic()
    {
        return magic;
    }
}
