package no.stelar7.api.l4j8.pojo.staticdata.shared;

public class Image
{
    String  full;
    String  group;
    Integer h;
    String  sprite;
    Integer w;
    Integer x;
    Integer y;

    /**
     * Gets the full.
     *
     * @return the full
     */
    public String getFull()
    {
        return full;
    }

    /**
     * Gets the group.
     *
     * @return the group
     */
    public String getGroup()
    {
        return group;
    }

    /**
     * Gets the h.
     *
     * @return the h
     */
    public Integer getH()
    {
        return h;
    }

    /**
     * Gets the sprite.
     *
     * @return the sprite
     */
    public String getSprite()
    {
        return sprite;
    }

    /**
     * Gets the w.
     *
     * @return the w
     */
    public Integer getW()
    {
        return w;
    }

    /**
     * Gets the x.
     *
     * @return the x
     */
    public Integer getX()
    {
        return x;
    }

    /**
     * Gets the y.
     *
     * @return the y
     */
    public Integer getY()
    {
        return y;
    }
}
