package no.stelar7.api.l4j8.pojo.staticdata.shared;

public class Gold
{
    Integer base;
    Boolean purchasable;
    Integer sell;
    Integer total;

    /**
     * Gets the base.
     *
     * @return the base
     */
    public Integer getBase()
    {
        return base;
    }

    /**
     * Gets the purchasable.
     *
     * @return the purchasable
     */
    public Boolean getPurchasable()
    {
        return purchasable;
    }

    /**
     * Gets the sell.
     *
     * @return the sell
     */
    public Integer getSell()
    {
        return sell;
    }

    /**
     * Gets the total.
     *
     * @return the total
     */
    public Integer getTotal()
    {
        return total;
    }
}
