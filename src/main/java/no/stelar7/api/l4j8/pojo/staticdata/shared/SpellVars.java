package no.stelar7.api.l4j8.pojo.staticdata.shared;

import java.util.List;

public class SpellVars
{
    List<Double> coeff;
    String dyn;
    String key;
    String link;
    String       ranksWith;

    /**
     * Gets the coeff.
     *
     * @return the coeff
     */
    public List<Double> getCoeff()
    {
        return coeff;
    }

    /**
     * Gets the dyn.
     *
     * @return the dyn
     */
    public String getDyn()
    {
        return dyn;
    }

    /**
     * Gets the key.
     *
     * @return the key
     */
    public String getKey()
    {
        return key;
    }

    /**
     * Gets the link.
     *
     * @return the link
     */
    public String getLink()
    {
        return link;
    }

    /**
     * Gets the ranks with.
     *
     * @return the ranks with
     */
    public String getRanksWith()
    {
        return ranksWith;
    }
    
}
