package no.stelar7.api.l4j8.pojo.staticdata.shared;

public class MetaData
{
    Boolean isRune;
    String  tier;
    String  type;

    /**
     * Gets the checks if is rune.
     *
     * @return the checks if is rune
     */
    public Boolean getIsRune()
    {
        return isRune;
    }

    /**
     * Gets the tier.
     *
     * @return the tier
     */
    public String getTier()
    {
        return tier;
    }

    /**
     * Gets the type.
     *
     * @return the type
     */
    public String getType()
    {
        return type;
    }
}
